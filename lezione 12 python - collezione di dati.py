# 3 - spiegazione termini
    #  1 - ordinato: la collezione ha ordine ben definito e l0aggiunta di elementi non incide
    #  2 - indicizzato: possiamo accedere agli elementi , cambiare indice (index)
    #  3 - modificabile: possiamo aggiungere , cambiare e rimuovere gli elementi
    #  4 - immutabili :non possiamo aggiungere , cambiare o rimuovere elementi
    #  5 - permettere duplicati: possono esserci più elementi con lo stesso valore


# 4 - le liste non sono collezioni ordinate e modificabili. permettono duplicati
# 5 - le tuple sono collezioni ordinate ma immutabili . permettono duplicati
# ------------------------------------------
# 6 - i set sono collezioni non ordinate e modificabili. non permettono duplicati
# 7 - i dictionary sono collezioni ordinate e modificabili(dalla versione 3.7). non permettono duplicati

